    <h3>SQL-инъекция</h3>
<p>Это уязвимость безопасности в приложениях, которая позволяет злоумышленнику вмешиваться в запросы к базе данных. Инъекция происходит, когда ввод пользователя обрабатывается и вставляется в SQL-запрос без должной проверки, что позволяет манипулировать запросом и выполнять нежелательные операции с базой данных.</p>
<p>Использование <code>Statement</code> с динамически вставляемыми значениями может привести к SQL-инъекциям. Если входные данные пользователя вставляются напрямую в запрос, злоумышленник может модифицировать запрос и получить доступ к данным, на которые у него нет прав. Например, если пользователь введет <code>username=''admin'' OR ''1''=''1''</code>, запрос будет выполнен с дополнительными нежелательными условиями.</p>
<p>Чтобы избежать SQL-инъекций, всегда используйте параметризированные запросы (например, <code>PreparedStatement</code> или <code>CallableStatement</code>) вместо прямой вставки значений в запросы. Это гарантирует, что данные пользователя будут безопасно обработаны как параметры, а не как часть SQL-кода.</p>


        <p>В JDBC важно выбирать правильный тип <code>Statement</code> в зависимости от задачи: <code>Statement</code> для простых запросов, <code>PreparedStatement</code> для параметризованных, а <code>CallableStatement</code> для работы с хранимыми процедурами.</p>


