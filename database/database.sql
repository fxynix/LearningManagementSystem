    <h2>3. CAP теорема и её влияние на репликацию и шардирование</h2>
    <p><strong>CAP теорема</strong> (Brewer, 2000) утверждает, что распределённая система может одновременно обеспечивать только два из трёх свойств:</p>
    <ul>
        <li><strong>Consistency (C)</strong> – линейная согласованность: все узлы видят одни и те же данные в один момент времени. То есть после успешной записи чтение с любого узла вернёт это значение.</li>
        <li><strong>Availability (A)</strong> – доступность: каждый запрос получает ответ (не обязательно последние данные), даже при отказе узлов. То есть система всегда готова обрабатывать запросы.</li>
        <li><strong>Partition tolerance (P)</strong> – устойчивость к разделению сети: система продолжает работать при потере связи между узлами. То есть разрыв сетевого соединения между двумя группами узлов не приводит к отказу всей системы.</li>
    </ul>
    <p>В реальных распределённых NoSQL-системах сетевые разделения неизбежны, поэтому приходится выбирать между C и A:</p>
    <ul>
        <li><strong>AP-системы</strong> (Cassandra, DynamoDB, CouchDB) – жертвуют строгой согласованностью ради доступности и устойчивости к разделениям. Используют eventual consistency, конфликты разрешаются принудительно или автоматически (LWW, CRDT).</li>
        <li><strong>CP-системы</strong> (HBase, MongoDB (по умолчанию с write concern majority), Redis Cluster в некоторых режимах) – обеспечивают согласованность, но могут стать недоступными для записи при сетевом разделении.</li>
        <li><strong>CA-системы</strong> – теоретически невозможны в распределённой среде, существуют только на одном узле (не распределены).</li>
    </ul>
    <div class="note">
        <strong>Важно:</strong> CAP-теорема не означает, что система всегда либо CP, либо AP. Многие базы данных позволяют настраивать уровень согласованности (например, Cassandra с QUORUM даёт сильную согласованность при нормальной работе, но при разделении может пожертвовать доступностью). Практический компромисс описывается <strong>PACELC</strong> (Partition/Else, Availability/Consistency, Latency).
    </div>

    <h2>4. Совместное использование репликации и шардирования</h2>
    <p>В реальных системах репликация и шардирование применяются вместе: <strong>шардирование</strong> обеспечивает масштабируемость записи и хранения, а <strong>репликация</strong> внутри каждого шарда – отказоустойчивость и доступность данных.</p>
    <p>Примеры:</p>
    <ul>
        <li><strong>MongoDB</strong> – каждый шард является replica set (минимум 3 узла: primary, secondary, arbiter/secondary). Это даёт и горизонтальное масштабирование, и высокую доступность внутри шарда.</li>
        <li><strong>Cassandra</strong> – кольцо узлов одновременно участвует и в шардировании (по partition key), и в репликации (RF определяет, сколько узлов хранят копию). Один узел может хранить данные нескольких разных диапазонов (за счёт vnodes).</li>
        <li><strong>Redis Cluster</strong> – мастер-узлы обслуживают определённые хэш-слоты, и у каждого мастера есть одна или несколько реплик.</li>
        <li><strong>Elasticsearch</strong> – индекс разбивается на первичные шарды (primary shards), каждый из которых может иметь реплики (replica shards). Реплики повышают доступность и пропускную способность чтения.</li>
    </ul>
    <p>Такая архитектура позволяет строить системы, которые одновременно выдерживают отказ нескольких узлов, масштабируются до тысяч узлов и обрабатывают петабайты данных.</p>

    <h2>Заключение</h2>
    <p>Репликация и шардирование – два столпа современных распределённых NoSQL-баз данных. Репликация повышает доступность и отказоустойчивость за счёт хранения нескольких копий данных на разных узлах. Шардирование обеспечивает горизонтальное масштабирование, разбивая данные на части и распределяя их по узлам. Понимание этих механизмов, их типов, моделей согласованности и влияния CAP-теоремы необходимо для проектирования отказоустойчивых и производительных приложений, работающих с большими объёмами данных.</p>
    <p>При выборе конкретной NoSQL-системы важно учитывать требования к консистентности, задержкам, схемам доступа и ожидаемой нагрузке, чтобы правильно сконфигурировать репликацию и шардирование.</p>
