    <h1>NoSQL: Репликация и шардирование</h1>
    <p>В распределённых NoSQL-системах для достижения высокой доступности, отказоустойчивости и горизонтальной масштабируемости используются два ключевых механизма – <strong>репликация</strong> и <strong>шардирование</strong>. Они часто применяются вместе, но решают разные задачи и имеют собственную терминологию, архитектуру и компромиссы.</p>

    <h2>1. Репликация данных в NoSQL</h2>
    <p><strong>Репликация</strong> – это процесс копирования данных с одного узла (или группы узлов) на другие узлы кластера. Основные цели:</p>
    <ul>
        <li>Обеспечение <strong>отказоустойчивости</strong> – при сбое одного или нескольких узлов данные остаются доступными на репликах.</li>
        <li>Повышение <strong>доступности</strong> – клиенты могут читать данные с любой реплики, даже если мастер временно недоступен.</li>
        <li>Распределение <strong>нагрузки чтения</strong> – запросы на чтение можно балансировать между несколькими репликами.</li>
        <li>Геораспределение – размещение реплик в разных дата-центрах для снижения задержек и обеспечения отказоустойчивости при катастрофах.</li>
    </ul>

    <h3>1.1 Типы репликации</h3>
    <table>
        <tr><th>Тип</th><th>Описание</th><th>Примеры</th></tr>
        <tr>
            <td>Master-slave<br>(ведущий-ведомый)</td>
