    <h3>Типы ResultSet</h3>
    <p>Есть несколько типов <code>ResultSet</code>, которые могут быть использованы в зависимости от требований:</p>
    <ul>
        <li><strong>ResultSet.TYPE_FORWARD_ONLY</strong> — курсор может перемещаться только вперед.</li>
