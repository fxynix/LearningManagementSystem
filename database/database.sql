    private DataSource dataSource;

    private UserDao(DataSource dataSource) { // (1)
        this.dataSource = dataSource;
    }

    public User findById(Integer id) {
        try (Connection connection = dataSource.getConnection()) { // (2)
               PreparedStatement selectStatement = connection.prepareStatement("select * from users where id =  ?");
               // TODO execute the select etc.
        }
    }

    public User findByFirstName(String firstName) {
        try (Connection connection = dataSource.getConnection()) { // (2)
               PreparedStatement selectStatement = connection.prepareStatement("select * from users where first_name =  ?");
               // TODO execute the select etc.
        }
    }
}
</pre
        >

        <ol>
            <li>
                Всякий раз, когда вызывающий создает новый <i>UserDao</i> через
                свой конструктор, вызывающий также должен передать
                действительный источник данных.
            </li>
            <li>
                Методы <i>findByX</i> будут просто использовать этот источник
                данных.
            </li>
        </ol>

        <p>
            С точки зрения <i>UserDao</i> это выглядит намного лучше. Он больше
            не знает ни о классе приложения, ни о том, как создавать сами
            источники данных. Он только объявляет, что «если вы хотите создать
            (то есть использовать) меня, вам нужно дать мне источник данных».
        </p>

        <p>
            Но что будет, если вы захотите запустить свое приложение. Если
            раньше вы могли вызывать «<i>new UserService()</i>», то теперь вам
            нужно обязательно вызвать новый <i>UserDao(dataSource)</i>.
        </p>

        <pre>
    public class MyApplication {

        public static void main(String[] args) {
            UserDao userDao = new UserDao(Application.INSTANCE.dataSource());
            User user1 = userDao.findById(1);
            User user2 = userDao.findById(2);
            // etc ...
        }
    }
</pre
        >

        <h3>Контейнеры для внедрения зависимостей</h3>

        <p>
            Следовательно, проблема в том, что вы, как программист, все еще
            создаете <i>UserDAO</i> с помощью их конструктора и, таким образом,
            устанавливаете зависимость DataSource вручную.
        </p>

        <p>
            Было бы хорошо, если бы какая-то программная знала, что ваш
            <i>UserDAO</i> имеет зависимость от конструктора <i>DataSource</i>,
            и знал, как ее создать. А затем эта сущность сконструирует для вас
            оба объекта: работающий <i>DataSource</i> и работающий
            <i>UserDao</i>?
        </p>

        <p>
            Эта сущность является контейнером внедрения зависимостей и является
            именно тем, что представляет собой среда Spring.
        </p>

        <h2>Контейнер Spring IOC / Dependency Injection [7]</h2>

        <p>
            Как уже упоминалось в самом начале, Spring Framework по своей сути
            является контейнером внедрения зависимостей, который управляет
            написанными вами классами и их зависимостями для вас. Рассмотрим,
            как это происходит.
        </p>

        <h3>ApplicationContext</h3>

        <p>
            Тот, кто контролирует все ваши классы и может управлять ими
            соответствующим образом, называется <i>ApplicationContext</i> в
            Spring.
        </p>

        <p>Нам нужно добиться следующего кода:</p>

        <pre>
import org.springframework.context.ApplicationContext;
import org.springframework.context.annotation.AnnotationConfigApplicationContext;
import javax.sql.DataSource;

public class MyApplication {

    public static void main(String[] args) {
        ApplicationContext ctx = new AnnotationConfigApplicationContext(someConfigClass); // (1)

        UserDao userDao = ctx.getBean(UserDao.class); // (2)
        User user1 = userDao.findById(1);
        User user2 = userDao.findById(2);

        DataSource dataSource = ctx.getBean(DataSource.class); // (3)
        // etc ...
    }
}
</pre
        >
        [9]

        <p>
            Здесь мы создаем наш Spring <i>ApplicationContext</i>.
            <i>ApplicationContext</i> может дать нам полностью
            сконфигурированный <i>UserDao</i>, то есть один с его набором
            зависимостей <i>DataSource</i>. <i>ApplicationContext</i> может
            также предоставить нам источник данных напрямую, который является
            тем же источником данных, который он устанавливает внутри
            <i>UserDao</i>.
        </p>

        <p>
            Теперь больше не нужно беспокоиться о создании классов, вы можете
            просто попросить <i>ApplicationContext</i> предоставить вам рабочие
            классы.
        </p>

        <h3>
            Что такое <i>ApplicationContextConfiguration</i>? Как построить
            <i>ApplicationContext</i> из конфигураций.
        </h3>

        <p>
            В приведенном выше коде мы помещаем переменную с именем
            <i>someConfigClass</i> в конструктор
            <i>AnnotationConfigApplicationContext</i>. Вот быстрое напоминание:
        </p>

        <pre>
import org.springframework.context.annotation.AnnotationConfigApplicationContext;
public class MyApplication {

    public static void main(String[] args) {
        ApplicationContext ctx = new AnnotationConfigApplicationContext(someConfigClass); // (1)
        // ...
    }
}
</pre
        >

        <p>
            То, что вы действительно хотите передать в конструктор
            <i>ApplicationContext</i>, это ссылка на класс конфигурации, который
            должен выглядеть следующим образом:
        </p>

        <pre>
import org.springframework.context.annotation.Bean;
import org.springframework.context.annotation.Configuration;

@Configuration
public class MyApplicationContextConfiguration {  // (1)

    @Bean
    public DataSource dataSource() {  // (2)
        MysqlDataSource dataSource = new MysqlDataSource();
        dataSource.setUser("root");
        dataSource.setPassword("s3cr3t");
        dataSource.setURL("jdbc:mysql://localhost:3306/myDatabase");
        return dataSource;
    }

    @Bean
    public UserDao userDao() { // (3)
        return new UserDao(dataSource());
    }

}
</pre
        >

        <ol>
            <li>
                У вас есть выделенный класс конфигурации
                <i>ApplicationContext</i>, помеченный аннотацией
                <i>@Configuration</i>. [27]
            </li>
            <li>
                У вас есть метод, который возвращает <i>DataSource</i> и
                аннотируется <i>@Bean</i> для Spring.
            </li>
            <li>
                У вас есть другой метод, который возвращает <i>UserDao</i> и
                создает указанный <i>UserDao</i>, вызывая метод bean-компонента
                <i>dataSource</i>.
            </li>
        </ol>

        <p>
            Этого класса конфигурации уже достаточно для запуска самого первого
            приложения Spring.
        </p>

        <pre>
    import org.springframework.context.ApplicationContext;
import org.springframework.context.annotation.AnnotationConfigApplicationContext;

public class MyApplication {

    public static void main(String[] args) {
        ApplicationContext ctx = new AnnotationConfigApplicationContext(MyApplicationContextConfiguration.class);
        UserDao userDao = ctx.getBean(UserDao.class);
        // User user1 = userDao.findById(1);
        // User user2 = userDao.findById(1);
        DataSource dataSource = ctx.getBean(DataSource.class);
    }
}
</pre
        >

        <p>
            Теперь рассмотрим, что именно Spring и
            <i>AnnotationConfigApplicationContext</i> делают с тем классом
            конфигурации, который вы написали.
        </p>

        <h2>
            Почему мы создали <i>AnnotationConfigApplicationContext</i>?
            Существуют ли другие классы <i>ApplicationContext</i>?
        </h2>

        <p>
            Существует много способов создания Spring <i>ApplicationContext</i>,
            например, с помощью файлов XML, аннотированных классов конфигурации
            Java или даже программно. Для внешнего мира это представлено через
            единый интерфейс <i>ApplicationContext</i>.
        </p>

        <p>
            Посмотрите на класс MyApplicationContextConfiguration сверху. Это
            класс Java, который содержит аннотации Spring. Вот почему вам
            необходимо создать аннотацию
            <i>AnnotationConfigApplicationContext</i>.
        </p>

        <p>
            Если вместо этого вы хотите создать свой
            <i>ApplicationContext</i> из файлов XML, вы должны создать
            <i>ClassPathXmlApplicationContext</i>.
        </p>

        <p>
            Есть и много других, но в современном приложении Spring вы обычно
            начинаете с контекста приложения, основанного на аннотациях.
        </p>

        <h3>Что делает аннотация <i>@Bean</i>? Что такое Spring Bean?</h3>

        <p>
            Вам необходимо думать о методах внутри вашего класса конфигурации
            <i>ApplicationContext</i> как о фабричных методах. На данный момент
            есть один метод, который знает, как создавать экземпляры
            <i>UserDao</i>, и один метод, который создает экземпляры
            <i>DataSource</i>.
        </p>

        <p>
            Эти экземпляры, которые создаются этими фабричными методами,
            называются bean-компонентами. Spring контейнер создал их и они
            находятся под его управлением.
        </p>

        <p>
            Но это приводит к вопросу: сколько экземпляров определенного
            компонента должно быть создано Spring?
        </p>

        <h3>Что такое Spring bean scope?</h3>

        <p>
            Сколько экземпляров DAO следует создать в Spring? Чтобы ответить на
            этот вопрос, вам нужно узнать о bean scope (область применения
            бина).
        </p>

        <ul>
            <li>
                Должен ли Spring создать <i>singleton</i>: все ваши DAO
                используют один и тот же источник данных?
            </li>
            <li>
                Должен ли Spring создать <i>prototype</i>: все ваши DAO получают
                свой собственный источник данных?
            </li>
            <li>
                Или ваши компоненты должны иметь еще более сложные области
                действия, например: новый источник данных для
                <i>HttpRequest</i>? Или за <i>HttpSession</i>? Или за
                <i>WebSocket</i>?
            </li>
        </ul>

        <pre>
import org.springframewosrk.context.annotation.Bean;
import org.springframework.context.annotation.Scope;
import org.springframework.context.annotation.Configuration;

@Configuration
public class MyApplicationContextConfiguration {

    @Bean
    @Scope("singleton")
    // @Scope("prototype") etc.
    public DataSource dataSource() {
        MysqlDataSource dataSource = new MysqlDataSource();
        dataSource.setUser("root");
        dataSource.setPassword("s3cr3t");
        dataSource.setURL("jdbc:mysql://localhost:3306/myDatabase");
        return dataSource;
    }
}
</pre
        >

        <p>
            Аннотация области (scope annotation) определяет, сколько экземпляров
            создаст Spring. И, как упоминалось выше, это довольно просто:
        </p>

        <ul>
            <li>
                <i>Scope("singleton")</i> → Ваш бин будет синглтоном, т.е. будет
                только один экземпляр.
            </li>
            <li>
                <i>Scope("prototype")</i> → Каждый раз, когда кому-то нужна
                ссылка на ваш компонент, Spring создает новый. (Здесь есть
                несколько предостережений, например, внедрения прототипов в
                синглтоны).
            </li>
            <li>
                <i>Scope("session")</i> → Для каждого сеанса HTTP пользователя
                будет создан один компонент.
            </li>
            <li>и т.п.</li>
        </ul>

        <p>
            Суть: большинство приложений Spring почти полностью состоят из
            одноэлементных bean-компонентов, в которые время от времени
            добавляются другие области действия bean-компонента (прототип,
            запрос, сессия, websocket и т.д.).
        </p>

        <p>
            Теперь, когда вы знаете о <i>ApplicationContext</i>, <i>Beans</i> &
            <i>Scopes</i>, рассмотрим зависимости или то, как наш
            <i>UserDAO</i> может получить <i>DataSource</i>.
        </p>

        <h3>Что такое Spring Java Config?</h3>

        <p>
            До сих пор вы явно настраивали свои bean-компоненты в конфигурации
            <i>ApplicationContext</i> с помощью аннотированных
            <i>@Bean</i> методов Java.
        </p>

        <p>
            Это то, что вы бы назвали Spring Java Config, в отличие от указания
            всего в XML, который исторически был подходом для Spring.
        </p>

        <p>
            Один вопрос: почему вы должны явно вызывать новый <i>UserDao()</i> с
            ручным вызовом <i>dataSource()</i>? Разве Spring не может понять все
            это сам?
        </p>

        <p>Вот где появляется другая аннотация <i>@ComponentScan</i>.</p>

        <h3>Что делает <i>@ComponentScan</i>?</h3>

        <p>
            Первое изменение, которое вам нужно применить к своей конфигурации
            контекста — это добавить к ней дополнительную аннотацию
            <i>@ComponentScan</i>.
        </p>

        <pre>
import org.springframework.context.annotation.Bean;
import org.springframework.context.annotation.ComponentScan;
import org.springframework.context.annotation.Configuration;

@Configuration
@ComponentScan  // (1)
public class MyApplicationContextConfiguration {

    @Bean
    public DataSource dataSource() {
        MysqlDataSource dataSource = new MysqlDataSource();
        dataSource.setUser("root");
        dataSource.setPassword("s3cr3t");
        dataSource.setURL("jdbc:mysql://localhost:3306/myDatabase");
        return dataSource;
    }

    // (2)

    // no more UserDao @Bean method!
}
</pre
        >

        <ol>
            <li>Мы добавили аннотацию <i>@ComponentScan</i>.</li>
            <li>
                Обратите внимание, что определение <i>UserDAO</i> теперь
                отсутствует в конфигурации контекста!
            </li>
        </ol>

        <p>
            То, что делает эта аннотация <i>@ComponentScan</i>, это сказать
            Spring: Посмотрите на все классы Java в том же пакете, что и
            конфигурация контекста, если они выглядят как Spring Bean!
        </p>

        <p>
            Это означает, что, если ваш <i>MyApplicationContextConfiguration</i>
            находится в пакете com.marcobehler, Spring будет сканировать каждый
            пакет, включая подпакеты, который начинается с com.marcobehler для
            поиска потенциальных компонентов Spring.
        </p>

        <p>
            Чтобы Spring узнал, что ваши класса являются бинами, ваши классы
            должны быть помечены аннотацией <i>@Component</i>.
        </p>

        <h3>Что делают аннотации <i>@Component</i> и <i>@Autowired</i>?</h3>

        <p>
            Давайте добавим аннотацию <i>@Component</i> к вашему <i>UserDAO</i>.
        </p>

        <pre>
import javax.sql.DataSource;
import org.springframework.stereotype.Component;

@Component
public class UserDao {

    private DataSource dataSource;

    private UserDao(DataSource dataSource) { // (1)
        this.dataSource = dataSource;
    }
}
</pre
        >

        <p>
            Это говорит Spring то же, что и методу <i>@Bean</i>, который вы
            написали ранее.
        </p>

        <p>
            Отсутствует только один маленький кусочек информации. Как Spring
            узнает, что он должен взять <i>DataSource</i>, который вы указали
            как метод <i>@Bean</i>, а затем создать новые <i>UserDAO</i> с этим
            конкретным <i>DataSource</i>?
        </p>

        <p>
            Это делается с помощью аннотации <i>@Autowired</i> [8]. Тогда, ваш
            окончательный код будет выглядеть следующим образом:
        </p>

        <pre>
import javax.sql.DataSource;
import org.springframework.stereotype.Component;
import org.springframework.beans.factory.annotation.Autowired;

@Component
public class UserDao {

    private DataSource dataSource;

    private UserDao(@Autowired DataSource dataSource) {
        this.dataSource = dataSource;
    }
}
</pre
        >

        <p>
            Теперь Spring имеет всю информацию, необходимую для создания
            bean-компонентов <i>UserDAO</i>:
        </p>

        <ol>
            <li>
                <i>UserDAO</i> аннотируется <i>@Component</i> → Spring создаст
                его [23]
            </li>
            <li>
                <i>UserDAO</i> имеет аргумент конструктора <i>@Autowired</i> →
                Spring автоматически внедрит источник данных, настроенный с
                помощью вашего метода <i>@Bean</i>
            </li>
            <li>
                Если в ваших конфигурациях Spring не было настроено ни одного
                источника данных, вы получите исключение
                <i>NoSuchBeanDefinition</i> во время выполнения.
            </li>
        </ol>

        <h3>Внедрение зависимости через конструктор и <i>Autowired</i></h3>

        <p>
            В более ранних версиях Spring (до 4.2) вам нужно было указывать
            <i>@Autowired</i>, чтобы внедрение зависимости работало.
        </p>

        <p>
            В более новых версиях Spring действительно достаточно умен, чтобы
            внедрять эти зависимости без явной аннотации <i>@Autowired</i> в
            конструкторе. Так что это также будет работать.
        </p>

        <pre>
@Component
public class UserDao {

    private DataSource dataSource;

    private UserDao(DataSource dataSource) {
        this.dataSource = dataSource;
    }
}
</pre
        >

        <p>
            Рассмотрим различные способы внедрения зависимостей — внедрение
            конструкторов — всего лишь один из них.
        </p>

        <h3>Что такое Field Injection? Что такое Setter Injection?</h3>

        <p>
            Spring не должен использовать конструктор для внедрения
            зависимостей.
        </p>

        <p>Он также может напрямую внедрять поля.</p>

        <pre>
import javax.sql.DataSource;
import org.springframework.stereotype.Component;
import org.springframework.beans.factory.annotation.Autowired;

@Component
public class UserDao {

    @Autowired
    private DataSource dataSource;

}
</pre
        >

        <p>Кроме того, Spring также может внедрять сеттеры.</p>

        <pre>
import javax.sql.DataSource;
import org.springframework.stereotype.Component;
import org.springframework.beans.factory.annotation.Autowired;

@Component
public class UserDao {

    private DataSource dataSource;

    @Autowired
    public void setDataSource(DataSource dataSource) {
        this.dataSource = dataSource;
    }

}
</pre
        >

        <p>
            Эти два стиля внедрения (поля, сеттеры) имеют тот же результат, что
            и внедрение конструктора: вы получите работающий Spring Bean.
        </p>

        <p>
            Но очевидно, что они отличаются друг от друга, что означает, что
            было очень много споров о том, какой стиль внедрения является лучшим
            и какой вы должны использовать в своем проекте.
        </p>

        <h2>
            Spring AOP (Аспектно-ориентированное программирование) [15] и прокси
        </h2>

        <p>
            Внедрение зависимостей позволяет создавать более структурированные
            программамы, но внедрение зависимости здесь и там не совсем то, в
            чем заключается суть экосистемы Spring. Давайте еще раз посмотрим на
            простую <i>ApplicationContextConfiguration</i>:
        </p>

        <pre>
import org.springframework.context.annotation.Bean;
import org.springframework.context.annotation.Configuration;

@Configuration
public class MyApplicationContextConfiguration {

    @Bean
    public UserService userService() { // (1)
        return new UserService();
    }
}
</pre
        >

        <p>
            Предположим, что <i>UserService</i> — это класс, который позволяет
            вам находить пользователей из таблицы базы данных или сохранять
            пользователей в этой таблице базы данных.
        </p>

        <p>Вот где проявляется скрытая особенность Spring:</p>

        <p>
            В этом контексте Spring читает конфигурацию, содержащую метод
            <i> @Bean </i>, который вы написали, и поэтому Spring знает, как
            создавать и внедрять компоненты <i>UserService</i>.
        </p>

        <p>
            Однако Spring может создавать что-то, кроме вашего класса
            <i>UserService</i>.
        </p>

        <h3>Spring может создавать прокси</h3>

        <p>
            Потому что под капотом любой метод Spring <i>@Bean</i> может вернуть
            вам то, что (в вашем случае) выглядит и ощущается как
            <i>UserService</i>, но на самом деле это не так.
        </p>

        <p>Он может вернуть вам прокси.</p>

        <p>
            Прокси в какой-то момент делегирует службу
            <i>UserService</i>, которую вы написали, но сначала он выполнит свою
            собственную функциональность.
        </p>

        <p>
            В частности, Spring по умолчанию создаст динамические прокси-серверы
            Cglib, которым не нужен интерфейс для работы прокси-серверов
            (например, внутренний механизм прокси-сервера JDK): вместо этого
            Cglib может прокси-классы посредством их подклассов на лету.
        </p>

        <h3>Почему Spring создает прокси?</h3>

        <p>
            Потому что это позволяет Spring дать вашим компонентам
            дополнительные функции без изменения кода. В сущности, это то, что
            является аспектно-ориентированным (или: AOP) программированием.
        </p>

        <p>
            Рассмотрим самый популярный пример AOP — аннотацию Spring
            <i>@Transactional</i>.
        </p>

        <h3>Spring аннотация <i>@Transactional</i></h3>

        <p>
            Ваша реализация <i>UserService</i> выше может выглядеть примерно
            так:
        </p>

        <pre>
import org.springframework.stereotype.Component;
import org.springframework.transaction.annotation.Transactional;

@Component
public class UserService {

    @Transactional           // (2)
    public User activateUser(Integer id) {  // (1)
        // execute some sql
        // send an event
        // send an email
    }
}
</pre
        >

        <ol>
            <li>
                Мы написали метод activUser, который при вызове должен выполнить
                некоторый SQL-запрос, чтобы обновить состояние пользователя в
                базе данных, возможно, отправить сообщение электронной почты или
                событие обмена сообщениями.
            </li>
            <li>
                <i>@Transactional</i> для этого метода сигнализирует Spring, что
                для работы этого метода необходимо открытое соединение с базой
                данных / транзакция и что указанная транзакция также должна быть
                зафиксирована в конце. И Spring должна сделать это. [18]
            </li>
        </ol>

        <p>
            Проблема: хотя Spring может создать ваш компонент
            <i>UserService</i> через конфигурацию <i>applicationContext</i>, он
            не может переписать ваш <i>UserService</i>. Он не может просто
            вставить туда код, который открывает соединение с базой данных и
            фиксирует транзакцию с базой данных.
        </p>

        <p>
            Но что она может сделать, так это создать прокси вокруг вашего
            <i>UserService</i>, который будет транзакционным. Таким образом,
            только прокси-сервер должен знать, как открывать и закрывать
            соединение с базой данных, а затем может просто делегировать его
            вашему <i>UserService</i>.
        </p>

        <p>
            Посмотрим на эту безопасную
            <i>ContextConfiguration</i>.
        </p>

        <pre>
@Configuration
@EnableTransactionManagement // (1)
public class MyApplicationContextConfiguration {

    @Bean
    public UserService userService() { // (2)
        return new UserService();
    }
}
</pre
        >

        <ol>
            <li>
                Мы добавили аннотацию, сигнализирующую Spring: да, нам нужна
                поддержка <i>@Transactional</i>, которая автоматически включает
                прокси Cglib под капотом.
            </li>
            <li>
                С указанным выше набором аннотаций Spring не просто создает и
                возвращает ваш <i>UserService</i> здесь. Он создает Cglib-прокси
                вашего компонента, который выглядит как ваш <i>UserService</i>,
                но фактически оборачивает ваш <i>UserService</i> и предоставляет
                свои функции управления транзакциями.
            </li>
        </ol>

        <p>
            Поначалу это может показаться немного не интуитивным, но большинство
            разработчиков Spring очень скоро сталкиваются с прокси в сеансах
            отладки. Из-за прокси трассировки стека Spring могут быть довольно
            длинными и незнакомыми: когда вы входите в метод, вы можете очень
            хорошо сначала войти в прокси — что отпугивает людей. Это совершенно
            нормальное и ожидаемое поведение.
        </p>

        <h3>Нужно ли Spring использовать прокси Cglib?</h3>

        <p>
            Прокси являются выбором по умолчанию при программировании AOP с
            помощью Spring. Однако вы не ограничены использованием прокси, вы
            также можете пройти полный маршрут AspectJ, который при желании
            изменяет ваш фактический байт-код.
        </p>

        <h2>Управление ресурсами Spring</h2>

        <p>
            Подумайте, как бы вы попытались получить доступ к файлу в Java через
            HTTP или FTP? Вы можете использовать класс URL в Java и написать
            некоторый код.
        </p>

        <p>
            Точно так же, как бы вы читали в файлах из пути к классам вашего
            приложения? Или из контекста сервлета, это означает из корневого
            каталога веб-приложений (по общему признанию, это становится все
            реже и реже в современном приложении packaged.jar).
        </p>

        <p>
            Опять же, вам нужно написать довольно много стандартного кода, чтобы
            он работал, и, к сожалению, код будет отличаться для каждого
            варианта использования (URL-адреса, пути к классам, контексты
            сервлетов).
        </p>

        <p>
            Но есть решение: абстракция ресурсов Spring. Это легко объяснить в
            коде.
        </p>

        <pre>
import org.springframework.core.io.Resource;

public class MyApplication {

    public static void main(String[] args) {
            ApplicationContext ctx = new AnnotationConfigApplicationContext(someConfigClass); // (1)

            Resource aClasspathTemplate = ctx.getResource("classpath:somePackage/application.properties"); // (2)

            Resource aFileTemplate = ctx.getResource("file:///someDirectory/application.properties"); // (3)

            Resource anHttpTemplate = ctx.getResource("https://marcobehler.com/application.properties"); // (4)

            Resource depends = ctx.getResource("myhost.com/resource/path/myTemplate.txt"); // (5)

            Resource s3Resources = ctx.getResource("s3://myBucket/myFile.txt"); // (6)
    }
}
</pre
        >

        <ol>
            <li>Вам нужен <i>ApplicationContext</i> для начала.</li>
            <li>
                Когда вы вызываете <i>getResource()</i> для
                <i>applicationContext</i> со строкой, которая начинается с
                <i>classpath</i>:, Spring будет искать ресурс в вашем
                <i>application</i> <i>classpath</i>.
            </li>
            <li>
                Когда вы вызываете <i>getResource()</i> со строкой, начинающейся
                с <i>file</i>:, Spring будет искать файл на вашем жестком диске.
            </li>
            <li>
                Когда вы вызываете <i>getResource()</i> со строкой, которая
                начинается с <i>https</i>: (или idhttp), Spring будет искать
                файл в Интернете.
            </li>
            <li>
                Если вы не укажете префикс, это зависит от того, какой тип
                контекста приложения вы настроили.
            </li>
            <li>
                Это не работает из коробки со Spring Framework, но с
                дополнительными библиотеками, такими как Spring Cloud, вы даже
                можете напрямую обращаться к путям s3://. [25, 48]
            </li>
        </ol>

        <p>
            Короче говоря, Spring дает вам возможность доступа к ресурсам с
            помощью приятного небольшого синтаксиса. Интерфейс ресурса имеет
            несколько интересных методов:
        </p>

        <pre>
public interface Resource extends InputStreamSource {

    boolean exists();

    String getFilename();

    File getFile() throws IOException;

    InputStream getInputStream() throws IOException;

    // ... other methods commented out
}
</pre
        >

        <p>
            Как видите, он позволяет вам выполнять самые распространенные
            операции с ресурсом:
        </p>

        <ol>
            <li>Это существует?</li>
            <li>Какое имя файла?</li>
            <li>Получить ссылку на фактический объект File.</li>
            <li>
                Получить прямую ссылку на необработанные данные (InputStream).
            </li>
        </ol>

        <p>
            Это позволяет вам делать все, что вы хотите с ресурсом, независимо
            от того, находится он в Интернете, на вашем пути к классам или на
            жестком диске.
        </p>

        <p>
            Абстракция ресурсов выглядит хорошо сочитаетсясо следующей функцией,
            предлагаемой Spring: <i>Properties</i>.
        </p>

        <h3>Что такое Spring Environment?</h3>

        <p>
            Большая часть любого приложения — это чтение свойств (properties),
            таких как имя пользователя и пароли базы данных, конфигурация
            почтового сервера, детализированная конфигурация оплаты и т.д.
        </p>

        <p>
            В простейшем виде эти свойства находятся в файлах
            <i>.properties</i>, и их может быть много:
        </p>

        <ul>
            <li>
                Некоторые из них на вашем <i>classpath</i>, так что у вас есть
                доступ к некоторым паролям, связанным с разработкой.
            </li>
            <li>
                Другие в файловой системе или на сетевом диске, поэтому рабочий
                сервер может иметь свои собственные, защищенные свойства.
            </li>
            <li>
                Некоторые могут даже прийти в форме переменных среды
                операционной системы.
            </li>
        </ul>

        <p>
            Spring пытается упростить регистрацию и автоматический поиск свойств
            во всех этих различных источниках с помощью абстракции
            <i>environment</i>.
        </p>

        <pre>
import org.springframework.core.env.Environment;
public class MyApplication {

    public static void main(String[] args) {
           ApplicationContext ctx = new AnnotationConfigApplicationContext(someConfigClass);
           Environment env = ctx.getEnvironment(); // (1)
           String databaseUrl = env.getProperty("database.url"); // (2)
           boolean containsPassword = env.containsProperty("database.password");
           // etc
    }
}
</pre
        >

        <p>
            Через <i>applicationContext</i> вы всегда можете получить доступ к
            текущей среды (environment) выполняемого Spring приложения. Среда
            (environment), с другой стороны, позволяет вам, помимо прочего,
            получать доступ к свойствам.
        </p>

        <h3>Что такое Spring <i>@PropertySources</i>?</h3>

        <p>
            Среда состоит из одного или многих источников свойств выполняемого
            Spring приложения. Например:
        </p>

        <ul>
            <li>/mydir/application.properties</li>
            <li>classpath:/application-default.properties</li>
        </ul>

        <p>
            По умолчанию среда веб-приложения Spring MVC состоит из параметра
            ServletConfig/Context, источников системных свойств JNDI и JVM. Они
            также являются иерархическими, это означает, что они имеют порядок
            важности и перекрывают друг друга.
        </p>

        <p>
            Тем не менее, довольно легко определить новые
            <i>@PropertySources</i>
            самостоятельно:
        </p>

        <pre>
import org.springframework.context.annotation.PropertySources;
import org.springframework.context.annotation.PropertySource;

@Configuration
@PropertySources(
        {@PropertySource("classpath:/com/${my.placeholder:default/path}/app.properties"),
         @PropertySource("file://myFolder/app-production.properties")})
public class MyApplicationContextConfiguration {
    // your beans
}
</pre
        >

        <p>
            Аннотация <i>@PropertySource</i> работает с любым допустимым классом
            конфигурации Spring и позволяет вам определять новые дополнительные
            источники с помощью абстракции ресурсов Spring. Помните, что все
            дело в префиксах http://, file://, classpath: и т.д.
        </p>

        <h3>Spring аннотация <i>@Value</i> и внедрение значений свойств</h3>

        <p>
            Вы можете внедрять значения свойств в ваши bean-компоненты, так же,
            как вы бы добавили зависимость с аннотацией <i>@Autowired</i>. Но
            для свойств вам нужно использовать аннотацию <i>@Value</i>. [20]
        </p>

        <pre>
import org.springframework.stereotype.Component;
import org.springframework.beans.factory.annotation.Value;

@Component
public class PaymentService {

    @Value("${paypal.password}")  // (1)
    private String paypalPassword;

     public PaymentService(@Value("${paypal.url}") String paypalUrl) { // (2)
         this.paypalUrl = paypalUrl;
    }
}
</pre
        >

        <p>
            Аннотация <i>@Value</i> работает непосредственно с полями или по
            аргументам конструктора.
        </p>

        <p>
            Всякий раз, когда вы используете аннотацию <i>@Value</i>, Spring
            будет проходить через вашу (иерархическую) среду и искать
            соответствующее свойство — или выдавать сообщение об ошибке, если
            такого свойства не существует.
        </p>

        <h2>Spring Web MVC</h2>

        <p>
            Spring Web MVC, также известный как Spring MVC, является веб-средой
            Spring. Это позволяет создавать все, что связано с сетью, от
            небольших веб-сайтов до сложных веб-сервисов. Он также поддерживает
            фреймворки, такие как Spring Boot. [11]
        </p>

        <h3>Что такое MVC?</h3>

        <p>
            В контексте рендеринга HTML-страниц, например, страницы учетной
            записи пользователя, вот как выглядит MVC в Spring:
        </p>

        <ul>
            <li>
                Ваша <i>Model</i> (модель) содержит данные, которые вы хотите
                отобразить на веб-странице. Однако данные полностью независимы
                от вашего HTML, это простые объекты Java (например, объекты
                пользователя), из которых состоит ваше приложение.
            </li>
            <li>
                Ваше <i>View</i> (представление) [13] будет HTML-шаблоном,
                который является каркасом для вашей HTML-страницы, написанной с
                определенной библиотекой шаблонов. Эти библиотеки позволяют
                включать заполнители в ваши шаблоны, которые позволяют получить
                доступ к данным модели, например, имени пользователя.
            </li>
            <li>
                <i>Controller</i> (Контроллер) будет аннотированным методом
                <i> @Controller </i>, который отвечает на HTTP-запрос /account и
                знает, как преобразовать HTTP-запрос в объекты Java, а также
                ваши объекты Java в ответ HTML. [10]
            </li>
        </ul>

        <p>
            Вашей конечной целью является написание контроллеров с помощью
            Spring, которые сопоставляются с конкретными HTTP-запросами, такими
            как (/account), и предоставляют пользователю соответствующую
            HTML-страницу, которая визуализируется путем объединения
            представления и данных, необходимых для этой страницы.
        </p>

        <p>Та же логика верна для написания веб-сервисов JSON или XML.</p>

        <h3>Как писать классы типа <i>Controller</i>?</h3>

        <p>
            Мы можем написать наш <i>Controller</i> класс, который обрабатывает
            запросы /account.
        </p>

        <p>
            Страница /account будет HTML-страницей с несколькими динамическими
            переменными, такими как имя пользователя, адрес, информация о
            подписке и т.д.
        </p>

        <pre>
import org.springframework.stereotype.Controller;
import org.springframework.ui.Model;
import org.springframework.web.bind.annotation.GetMapping;
import org.springframework.web.bind.annotation.PathVariable;

/**
*  A class that responds to /account requests.
*  Think of Netflix''s account page, where you want to see your username/password/subscription info
*/
@Controller // (1)
public class AccountController {

    @GetMapping("/account/{userId}")
    public String account(@PathVariable Integer userId) { // (2)
        // TODO retrieve name, address, subscription information
        return "templates/account"; // (3)
    }
}
</pre
        >

        <p>
            В этих двух строчках происходит МНОГО, давайте посмотрим, что
            именно.
        </p>

        <p>
            У вас есть класс <i>AccountController</i>, который аннотируется
            <i>@Controller</i>. Он сообщает Spring: этот класс хочет реагировать
            на HTTP-запросы и ответы, чтобы <i>DispatcherServlet</i> знал об
            этом.
        </p>

        <p>
            У вас есть простой Java-метод, называемый account(). Что еще
            интереснее, метод аннотируется <i>@GetMapping</i> [14]. Это сообщает
            DispatcherServlet, что все запросы, похожие на /account/{userId},
            должны обрабатываться именно этим методом контроллера. Более того,
