    <h3>1.4 Репликация в Apache Cassandra</h3>
    <p>Cassandra – децентрализованная NoSQL БД с multi-master репликацией. То есть нет единого узла, принимающего запись: любой узел (координатор) может принять запись от клиента и переслать её всем узлам, которые должны хранить данную копию в соответствии с <strong>фактором репликации (RF)</strong>.</p>
    <ul>
        <li><strong>Фактор репликации (RF)</strong> – количество копий каждого куска данных в кластере. RF=3 означает три копии. Копии размещаются на разных узлах, чтобы при отказе одного узла данные оставались доступными.</li>
        <li><strong>Стратегии репликации</strong>:
            <ul>
                <li><em>SimpleStrategy</em> – для одного дата-центра, копии размещаются на последовательных узлах в кольце.</li>
                <li><em>NetworkTopologyStrategy</em> – для нескольких дата-центров, позволяет задать отдельный RF для каждого ДЦ. Это обеспечивает отказоустойчивость на уровне дата-центра.</li>
            </ul>
        </li>
        <li><strong>Gossip-протокол</strong> – используется для обнаружения узлов, обмена состоянием и метаданными (heartbeat, статус узла: up/down). Каждую секунду каждый узел обменивается информацией с 1–3 другими случайными узлами, так что состояние распространяется по кластеру.</li>
        <li><strong>Hinted handoff</strong> – если узел-получатель недоступен, координатор временно сохраняет запись (хинт) и отправляет её, когда узел возвращается. Хинты хранятся ограниченное время (по умолчанию 3 часа) и предотвращают потерю данных при кратковременных отключениях.</li>
        <li><strong>Read repair</strong> – при чтении данных Cassandra сравнивает версии с разных реплик и при расхождении обновляет устаревшие. Это улучшает консистентность.</li>
        <li><strong>Anti-entropy (Merkle trees)</strong> – фоновая процедура сверки деревьев хэшей (Merkle) для обнаружения и исправления давних рассинхронизаций, которые могли возникнуть, например, из-за длительного отказа узла и потери хинтов.</li>
    </ul>
    <div class="code">CREATE KEYSPACE my_keyspace
WITH replication = {
  ''class'': ''NetworkTopologyStrategy'',
  ''DC1'': 3,
  ''DC2'': 2
};

