    <h3>1.2 Модели согласованности при репликации</h3>
    <p>В распределённых системах репликация вносит компромисс между согласованностью, доступностью и задержкой (CAP-теорема).</p>
    <ul>
        <li><strong>Строгая согласованность (Strong consistency)</strong> – после успешной записи все последующие чтения (с любого узла) будут видеть это значение. Достигается синхронной репликацией или использованием кворумов. Обеспечивает линейность, но увеличивает задержку и снижает доступность при сетевых разделениях.</li>
        <li><strong>Согласованность в конечном счёте (Eventual consistency)</strong> – после записи данные могут временно расходиться между узлами, но при отсутствии новых записей все реплики гарантированно сойдутся к одному значению. Обеспечивает высокую доступность и малую задержку, но допускает чтение устаревших данных.</li>
        <li><strong>Монотонное чтение / Монотонная запись</strong> – более слабые гарантии, но полезные для некоторых приложений.</li>
    </ul>

    <h3>1.3 Репликация в MongoDB (Replica Set)</h3>
    <p>MongoDB использует модель master-slave с автоматическим переключением при отказе (failover). То есть в группе узлов, называемой <em>replica set</em>, один узел является первичным (primary) и принимает все операции записи, а остальные вторичные (secondary) асинхронно реплицируют изменения. Если primary становится недоступным, secondary автоматически проводят выборы и назначают новый primary, обеспечивая непрерывность обслуживания.</p>

    <p><strong>Replica set</strong> – это группа узлов, хранящих один и тот же набор данных. Она состоит из следующих типов узлов:</p>
    <ul>
        <li><strong>Primary (первичный)</strong> – единственный узел, который принимает операции записи. Все изменения записываются в специальную коллекцию – <strong>oplog</strong> (журнал операций). Primary также может обслуживать чтение.</li>
        <li><strong>Secondary (вторичный)</strong> – узлы, которые постоянно читают oplog primary и применяют операции к своим данным, оставаясь точной копией. Secondary могут отвечать на запросы чтения (если это разрешено настройками read preference), что позволяет распределять нагрузку.</li>
        <li><strong>Arbiter (арбитр)</strong> – специальный узел, который не хранит данные, но участвует в выборах primary, чтобы избежать ситуации «чётное число голосов». Например, в replica set из двух узлов при отказе одного невозможно получить большинство, арбитр решает проблему.</li>
    </ul>

    <p>Ключевые механизмы MongoDB replica set:</p>
    <ul>
        <li><strong>Oplog</strong> – ограниченная коллекция (capped collection), в которой хранятся все операции, изменяющие данные. Размер oplog задаётся администратором. Secondary синхронизируются, читая oplog с primary. Если secondary отстал слишком сильно и oplog уже перезаписан, требуется полная повторная синхронизация.</li>
        <li><strong>Write concern</strong> – параметр, определяющий, сколько узлов replica set должны подтвердить запись, прежде чем она будет считаться успешной. <code>w: 1</code> – только primary, <code>w: "majority"</code> – большинство узлов (включая primary), <code>w: 3</code> – три узла. Это позволяет контролировать консистенцию.</li>
        <li><strong>Read preference</strong> – настройка, указывающая, с каких узлов клиент должен читать: primary, secondary, nearest, primaryPreferred и т.д. Например, для отчётных систем можно использовать <code>secondaryPreferred</code>, чтобы не нагружать primary.</li>
        <li><strong>Выборы (election)</strong> – когда primary становится недоступным, secondary инициируют выборы. Узел с наибольшим приоритетом и актуальными данными становится новым primary. Выборы используют протокол на основе Raft (начиная с MongoDB 3.2).</li>
    </ul>
