        <h2>Обработка пакетов в JDBC</h2>

        <p>Обработка пакетов (batch processing) позволяет объединить несколько SQL-запросов в одну логическую группу и отправить их в базу данных за один запрос.</p>

        <h3>Зачем использовать обработку пакетов?</h3>
        <p>Когда вы отправляете несколько SQL-запросов в базу данных как часть одного пакета, это позволяет существенно уменьшить нагрузку на сеть и ускорить выполнение запросов. Вместо того, чтобы отправлять каждый запрос по одному, вы объединяете их в группу и отправляете все сразу.</p>

        <h3>Как использовать обработку пакетов в JDBC?</h3>
        <p>Для использования обработки пакетов в JDBC, выполните следующие шаги:</p>
        <ol>
            <li>Создайте экземпляр одного из следующих объектов:
                <ul>
                    <li>Statement</li>
                    <li>PreparedStatement</li>
                    <li>CallableStatement</li>
                </ul>
            </li>
            <li>Убедитесь, что ваш JDBC-драйвер поддерживает обработку пакетов, используя метод <code>DatabaseMetaData.supportBatchUpdates()</code>.</li>
            <li>Отключите автокоммит с помощью <code>setAutoCommit(false)</code>.</li>
            <li>Добавьте SQL-запросы в пакет с помощью метода <code>addBatch()</code>.</li>
            <li>Выполните все запросы в пакете с помощью <code>executeBatch()</code>.</li>
            <li>Сохраните изменения с помощью <code>commit()</code>.</li>
        </ol>

        <h3></h3>

        <div class="code">public class BatchExample {
    public static void main(String[] args) {
        try (Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/mydb", "user", "password");
                Statement stmt = conn.createStatement()) {
            conn.setAutoCommit(false);  // Отключаем автокоммит

            stmt.addBatch("INSERT INTO employees (name) VALUES (''John'')");
            stmt.addBatch("INSERT INTO employees (name) VALUES (''Alice'')");
            stmt.executeBatch();  // Выполняем пакет

            conn.commit();  // Сохраняем изменения
            System.out.println("Пакет выполнен.");
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }
}</div>


