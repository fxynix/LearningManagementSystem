    <p>Cassandra предлагает настраиваемую согласованность для чтения и записи: ONE (одна реплика), QUORUM (большинство), ALL (все), LOCAL_QUORUM (большинство в локальном ДЦ) и другие. Это позволяет разработчику выбирать между строгостью и производительностью.</p>

    <h3>1.5 Репликация в Redis</h3>
    <p>Redis поддерживает асинхронную репликацию master-slave. То есть один мастер обрабатывает все записи, а реплики копируют данные с задержкой. Реплики могут обслуживать запросы чтения, снижая нагрузку на мастер. Однако при отказе мастера запись станет невозможной, если не используется автоматическое переключение.</p>
    <ul>
        <li><strong>Redis Sentinel</strong> – система мониторинга, которая обеспечивает автоматический failover: обнаруживает отказ мастера, выбирает нового из реплик и перенастраивает клиентов. Sentinel также предоставляет сервис обнаружения (service discovery) для клиентов.</li>
        <li><strong>Redis Cluster</strong> – решение, объединяющее шардирование (хэш-слоты) и репликацию. Каждый мастер-узел имеет одну или несколько реплик. При отказе мастера его реплика продвигается автоматически (благодаря внутреннему механизму голосования).</li>
    </ul>
    <div class="code"># Настройка реплики в redis.conf
replicaof 192.168.1.1 6379

# Пример Sentinel конфигурации для мониторинга
sentinel monitor mymaster 192.168.1.1 6379 2
sentinel down-after-milliseconds mymaster 5000
sentinel failover-timeout mymaster 60000</div>

    <h2>2. Шардирование (Sharding) в NoSQL</h2>
    <p><strong>Шардирование</strong> – это метод горизонтального разделения данных: большая таблица/коллекция разбивается на более мелкие части (шарды), каждая из которых хранится на отдельном сервере (или на реплика-сете). Цель – преодолеть ограничения одного узла по объёму данных, пропускной способности записи и оперативной памяти. То есть вместо того чтобы увеличивать мощности одного сервера (вертикальное масштабирование), мы добавляем больше серверов и распределяем данные между ними.</p>

    <h3>2.1 Ключ шардирования (Shard Key)</h3>
    <p>Ключ шардирования – это атрибут (или набор атрибутов), на основе которого система определяет, в какой шард попадает строка/документ. Выбор правильного ключа – критически важен:</p>
    <ul>
        <li>Он должен обеспечивать <strong>равномерное распределение</strong> данных, чтобы избежать «горячих точек» (hotspot).</li>
        <li>Должен использоваться во многих запросах, чтобы маршрутизация была эффективной. Если запрос не содержит ключ шардирования, системе придётся опрашивать все шарды (scatter/gather).</li>
        <li>Не должен изменяться (обновление ключа шардирования может быть очень дорогим или невозможным).</li>
    </ul>

    <h3>2.2 Стратегии шардирования</h3>
    <table>
        <tr><th>Стратегия</th><th>Принцип</th><th>Плюсы</th><th>Минусы</th></tr>
        <tr>
            <td>Range-based</td>
            <td>Данные разбиваются на диапазоны значений ключа (например, ID от 1 до 1000 – шард A, 1001–2000 – шард B). То есть каждый шард отвечает за непрерывный интервал ключей.</td>
            <td>Эффективен для запросов по диапазону, простота реализации.</td>
            <td>Риск дисбаланса (если данные неравномерно распределены), возможность создания «горячего» диапазона (например, последние ID).</td>
        </tr>
        <tr>
            <td>Hash-based</td>
            <td>Хэш-функция от ключа определяет номер шарда (часто по модулю). То есть каждый шард получает псевдослучайное подмножество ключей.</td>
            <td>Обеспечивает статистически равномерное распределение, избегает горячих точек.</td>
            <td>Запросы по диапазону ключей невозможно выполнить без отправки на все шарды (scatter/gather).</td>
        </tr>
        <tr>
            <td>Directory-based</td>
            <td>Используется внешний каталог (lookup service), который хранит маппинг «ключ → шард». То есть система не вычисляет шард по формуле, а обращается к справочнику.</td>
            <td>Максимальная гибкость, возможность произвольного перераспределения (например, переместить ключ на другой шард вручную).</td>
            <td>Дополнительная задержка на обращение к каталогу, единая точка отказа (каталог должен быть реплицирован).</td>
        </tr>
    </table>

    <h3>2.3 Consistent hashing и виртуальные узлы</h3>
    <p><strong>Consistent hashing</strong> – метод распределения данных по узлам, минимизирующий перемещение данных при добавлении или удалении узлов. То есть при изменении числа узлов только небольшая доля ключей (около 1/N) переезжает на новые узлы, остальные остаются на месте. Все возможные хэш-значения (например, от 0 до 2^32-1) располагаются на кольце. Каждому узлу соответствует один или несколько диапазонов (токенов).</p>
    <p><strong>Виртуальные узлы (vnodes)</strong> – каждый физический узел представляется множеством виртуальных узлов на кольце. Например, физический узел может быть представлен 256 виртуальными узлами, равномерно разбросанными по кольцу. Это улучшает балансировку, особенно когда физические узлы разнородны (можно назначить разное количество vnodes разным узлам). Используется в Cassandra, DynamoDB, Voldemort.</p>

    <h3>2.4 Балансировка и перебалансировка (Rebalancing)</h3>
    <p>В распределённых системах шарды должны быть сбалансированы по объёму данных и нагрузке. При добавлении новых узлов или сильном перекосе запускается <strong>перебалансировка</strong> – перемещение некоторых шардов (или их частей) с перегруженных узлов на новые.</p>
    <ul>
        <li>В <strong>MongoDB</strong> автоматический <strong>balancer</strong> периодически проверяет количество <strong>чанков (chunks)</strong> на шардах и при необходимости перемещает чанки с одного шарда на другой. Чанк – это непрерывный диапазон значений шард-ключа (по умолчанию 64 МБ). Миграция чанков происходит в фоне, без остановки обслуживания.</li>
        <li>В <strong>Cassandra</strong> при добавлении узла администратор запускает <code>nodetool rebuild</code> (для перемещения данных с существующих узлов) или <code>nodetool move</code> (для изменения диапазона токенов). Виртуальные узлы упрощают балансировку, так как достаточно перераспределить несколько vnodes.</li>
        <li>В <strong>Elasticsearch</strong> – шарды (shards) могут перемещаться между узлами автоматически под управлением кластерного координатора (cluster manager).</li>
    </ul>
    <div class="code"># MongoDB: ручное включение балансировщика
sh.startBalancer()

# Просмотр статуса чанков
sh.status()

# Cassandra: перестроение данных при добавлении нового узла
nodetool rebuild -- <new_node_ip></div>

    <h3>2.5 Проблемы и ограничения шардирования</h3>
    <ul>
        <li><strong>Горячие точки (hotspot)</strong> – если отдельный шард-ключ или диапазон получает непропорционально много запросов. Например, последние активные пользователи или популярная запись. Решение: использовать хэш-шардирование или составной ключ, включающий высококардинальный атрибут.</li>
        <li><strong>Сложность кросс-шардовых операций</strong> – JOIN, транзакции, атомарные обновления между разными шардами требуют координации (2PC) или невозможны. Многие NoSQL базы данных ограничивают такие операции (например, MongoDB поддерживает транзакции внутри шарда и с версии 4.2 – распределённые транзакции, но с оговорками).</li>
        <li><strong>Решардинг (изменение ключа шардирования)</strong> – крайне дорогостоящая операция, часто требует миграции всех данных. Поэтому выбор ключа шардирования – это важное решение на этапе проектирования.</li>
        <li><strong>Необходимость эластичного масштабирования без даунтайма</strong> – хотя современные системы стараются минимизировать простои, перемещение больших объёмов данных может влиять на производительность. Планирование перебалансировки в часы наименьшей нагрузки – общая практика.</li>
    </ul>

    <h3>2.6 Шардирование в MongoDB</h3>
    <p>MongoDB поддерживает автоматическое шардирование. Архитектура включает:</p>
    <ul>
        <li><strong>Шарды (shards)</strong> – каждый шард является replica set (для отказоустойчивости). То есть данные одного шарда реплицируются внутри себя, а разные шарды хранят разные диапазоны данных.</li>
        <li><strong>Маршрутизаторы (mongos)</strong> – принимают запросы от клиентов, определяют целевой шард на основе метаданных и направляют запрос, агрегируют результаты. Может быть несколько экземпляров mongos для балансировки нагрузки.</li>
        <li><strong>Серверы конфигурации (config servers)</strong> – хранят метаданные: какие диапазоны ключей (чанки) находятся на каких шардах, информацию о балансировке. Config servers сами представляют собой replica set (3 узла).</li>
    </ul>
    <div class="code"># Включение шардирования на базе данных
sh.enableSharding("somedb")

# Шардирование коллекции по хэшированному полю user_id (рекомендуется для равномерности)
sh.shardCollection("somedb.users", { "user_id": "hashed" })

# Или по диапазону (range) – поле timestamp
sh.shardCollection("somedb.events", { "timestamp": 1 })</div>
    <p>Выбор между хэш-шардированием и диапазонным – компромисс между равномерностью распределения и эффективностью запросов по диапазону. Для коллекций с монотонно возрастающими ключами (ObjectId, timestamp) хэш-шардирование предотвращает создание горячей точки на последнем шарде.</p>

    <h3>2.7 Шардирование в Cassandra</h3>
    <p>Cassandra изначально проектировалась как распределённая система без единой точки отказа. Каждая таблица имеет <strong>первичный ключ</strong>, первая часть которого – <strong>partition key</strong> – отвечает за шардирование. То есть хэш от partition key определяет, на каком узле (или группе узлов) будет храниться строка. Вторичные индексы и запросы без partition key приводят к полному сканированию кластера (scatter/gather), что неэффективно.</p>
    <p>Cassandra также поддерживает <strong>кластеризацию (clustering columns)</strong> для упорядочивания внутри партиции, что позволяет эффективно выполнять range-запросы внутри одной партиции. Например, для датчиков можно сделать partition key = sensor_id, а clustering column = timestamp, тогда данные конкретного датчика будут храниться вместе и отсортированы по времени.</p>
    <div class="code">CREATE TABLE sensors (
  sensor_id int,
  timestamp timestamp,
  value float,
  PRIMARY KEY (sensor_id, timestamp)
);
-- Здесь partition key = sensor_id, clustering key = timestamp
-- Данные одного sensor_id будут храниться на одном узле (в одной партиции), отсортированы по timestamp</div>

    <h3>2.8 Шардирование в HBase / Bigtable</h3>
    <p>HBase разбивает таблицы на <strong>регионы (regions)</strong> по диапазонам row key. Каждый регион обслуживается одним region server. При превышении размера региона (по умолчанию 10 ГБ) он автоматически разделяется на два (region split). То есть шардирование в HBase – это динамическое разбиение по диапазонам, которое не требует ручного вмешательства.</p>
    <p>Клиенты общаются с HBase через специальный каталог (метатаблицу), которая хранит местоположение регионов. Это напоминает directory-based шардирование. Метатаблица хранится на специальных серверах (Master).</p>
    <div class="code">create ''user_events'', ''cf''
