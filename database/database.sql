<h2>6. Дополнительные аспекты конфигурации Spring</h2>

<h3>6.1. Жизненный цикл бина: методы инициализации и уничтожения</h3>
<p>Spring позволяет выполнять пользовательскую логику после создания бина (инициализация) и перед его уничтожением. Это полезно для настройки ресурсов, проверки состояния или закрытия соединений.</p>
<p>В конфигурационном классе (аннотированном <code>@Configuration</code>) при объявлении бина с помощью <code>@Bean</code> можно указать метод инициализации с помощью атрибута <code>initMethod</code> и метод уничтожения с помощью атрибута <code>destroyMethod</code>.</p>
<pre><code>
import org.springframework.context.annotation.Bean;
import org.springframework.context.annotation.Configuration;

@Configuration
public class AppConfig {

    @Bean(initMethod = "customInit", destroyMethod = "customDestroy")
    public MyBean myBean() {
        return new MyBean();
    }
}

class MyBean {
    public void customInit() {
        System.out.println("Бин создан, инициализация...");
    }
    public void customDestroy() {
        System.out.println("Бин уничтожается, освобождение ресурсов...");
    }
}
</code></pre>
<p>Также можно использовать аннотации <code>@PostConstruct</code> и <code>@PreDestroy</code> (из Java EE / Jakarta EE), которые Spring автоматически обнаруживает. Однако атрибуты <code>initMethod</code> и <code>destroyMethod</code> — явный и безопасный способ, особенно если код бина не может быть изменён.</p>

<h3>6.2. Конфигурация веб-контекста в Spring MVC: WebMvcConfigurer</h3>
<p>Для настройки веб-слоя (например, добавление перехватчиков, обработка статических ресурсов, настройка преобразователей сообщений) в Spring MVC используется интерфейс <code>WebMvcConfigurer</code>. Класс, реализующий этот интерфейс (обычно с аннотацией <code>@Configuration</code>), позволяет переопределить стандартные настройки Spring MVC без использования XML.</p>
<pre><code>
import org.springframework.context.annotation.Configuration;
import org.springframework.web.servlet.config.annotation.WebMvcConfigurer;
import org.springframework.web.servlet.config.annotation.InterceptorRegistry;
import org.springframework.web.servlet.config.annotation.ResourceHandlerRegistry;

@Configuration
public class MyWebConfig implements WebMvcConfigurer {

    @Override
    public void addInterceptors(InterceptorRegistry registry) {
        registry.addInterceptor(new MyInterceptor());
    }

    @Override
    public void addResourceHandlers(ResourceHandlerRegistry registry) {
        registry.addResourceHandler("/static/**")
                .addResourceLocations("classpath:/static/");
    }
}
</code></pre>
<p>Более старый способ — наследование от <code>WebMvcConfigurerAdapter</code> (устарел в Spring 5). Современный подход — реализация интерфейса <code>WebMvcConfigurer</code> напрямую.</p>

<h3>6.3. Регистрация конфигурационного класса через AnnotationConfigApplicationContext</h3>
<p>Класс <code>AnnotationConfigApplicationContext</code> используется для создания контейнера Spring на основе Java-конфигурации. Для добавления одного или нескольких конфигурационных классов (аннотированных <code>@Configuration</code>) используется метод <code>register()</code>. После регистрации нужно вызвать <code>refresh()</code> для инициализации контекста.</p>
<pre><code>
import org.springframework.context.annotation.AnnotationConfigApplicationContext;

public class Main {
    public static void main(String[] args) {
        AnnotationConfigApplicationContext context = new AnnotationConfigApplicationContext();
        context.register(AppConfig.class);        // регистрация конфигурационного класса
        context.refresh();                         // применить конфигурацию
        MyBean bean = context.getBean(MyBean.class);
        context.close();
    }
}
</code></pre>
<p>Также можно передать классы в конструктор — тогда <code>refresh()</code> вызывается автоматически: <code>new AnnotationConfigApplicationContext(AppConfig.class);</code>. Но метод <code>register()</code> полезен, когда конфигурационные классы добавляются динамически или после создания контекста.</p>

<h3>6.4. Основная аннотация Spring Boot приложения: @SpringBootApplication</h3>
<p>В Spring Boot главный класс приложения помечается аннотацией <code>@SpringBootApplication</code>. Это составная аннотация, которая включает:</p>
<ul>
    <li><code>@Configuration</code> — класс является источником конфигурации.</li>
    <li><code>@EnableAutoConfiguration</code> — включает механизм автоматической конфигурации Spring Boot.</li>
    <li><code>@ComponentScan</code> — сканирует компоненты в текущем пакете и подпакетах.</li>
</ul>
<p>Таким образом, достаточно одной аннотации, чтобы запустить Spring Boot приложение.</p>
<pre><code>
import org.springframework.boot.SpringApplication;
import org.springframework.boot.autoconfigure.SpringBootApplication;

@SpringBootApplication
public class MySpringBootApplication {
    public static void main(String[] args) {
        SpringApplication.run(MySpringBootApplication.class, args);
    }
}
</code></pre>
<p>Использование только <code>@Configuration</code> не даст автоконфигурации и сканирования, поэтому для Spring Boot обязательна именно <code>@SpringBootApplication</code>.</p>

