    <h2>Транзакции</h2>
    <p>Это последовательность операций, выполняемых как единое целое. Если хотя бы одна операция не выполнена успешно, вся транзакция должна быть отменена. В контексте базы данных транзакция обычно включает в себя несколько SQL-запросов.</p>

    <h3>Основные принципы работы с транзакциями</h3>
    <p>Транзакции в JDBC поддерживают четыре основных свойства, которые называются ACID (Atomicity, Consistency, Isolation, Durability):</p>
    <ul>
        <li><strong>Atomicity</strong>: Все операции внутри транзакции выполняются как единое целое. Если одна операция не выполнена, вся транзакция откатывается.</li>
        <li><strong>Consistency</strong>: Транзакция переводит базу данных из одного согласованного состояния в другое.</li>
        <li><strong>Isolation</strong>: Изолирует транзакции от других транзакций, предотвращая проблемы, такие как потеря данных.</li>
        <li><strong>Durability</strong>: После завершения транзакции изменения становятся постоянными, даже если в системе произошел сбой.</li>
    </ul>

    <h3>Как использовать транзакции в JDBC?</h3>
    <p>Для работы с транзакциями в JDBC используется несколько методов. Основной метод — это управление автокоммитом с помощью метода <code>setAutoCommit(false)</code> и завершение транзакции с помощью методов <code>commit()</code> и <code>rollback()</code>.</p>

    <h3>Шаги работы с транзакциями в JDBC:</h3>
    <ol>
        <li>Отключите автокоммит: <code>connection.setAutoCommit(false);</code></li>
        <li>Выполните необходимые SQL-запросы.</li>
        <li>Если все запросы прошли успешно, завершите транзакцию методом <code>commit()</code>.</li>
        <li>Если произошла ошибка, откатите транзакцию методом <code>rollback()</code>.</li>
    </ol>
    <p>Использование транзакций в JDBC позволяет гарантировать целостность данных при выполнении нескольких операций. Вы всегда должны управлять транзакциями вручную, когда требуется, чтобы все изменения были выполнены или откатились полностью.</p>


