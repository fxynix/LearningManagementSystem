        <li><strong>Методы навигации:</strong> используются для перемещения указателя. Курсор двигается на основе свойств <code>ResultSet</code>, которые определяются при создании экземпляра.</li>
    </ul>
    <p>Курсор двигается на основе свойств ResultSet. Эти свойства указываются при создании экземпляра ResultSet.</p>
    <h3>Определение свойств ResultSet</h3>
    <p>Для определения свойств используются следующие методы:</p>
    <ul>
        <li><code>createStatement(int RSType, int RSConcurrency);</code></li>
        <li><code>prepareStatement(String SQL, int RSType, int RSConcurrency);</code></li>
        <li><code>prepareCall(String SQL, int RSType, int RSConcurrency);</code></li>
