</pre
        >

        <p>Это улучшение по двум направлениям:</p>

        <ol>
            <li>
                Вашему <i>UserDAO</i> больше не нужно создавать свою собственную
                зависимость <i>DataSource</i>, вместо этого он может попросить
                класс <i>Application</i> предоставить ему полнофункциональную.
                То же самое для всех других ваших DAO.
            </li>
            <li>
                Ваш класс приложения является одноэлементным (это означает, что
                будет создан только один INSTANCE), и этот одноэлементный
                компонент приложения содержит ссылку на одноэлементный объект
                <i>DataSource</i>.
            </li>
        </ol>

        <p>Однако у этого решения есть еще несколько недостатков:</p>

        <ol>
            <li>
                <i>UserDAO</i> активно должен знать, где получить свои
                зависимости, он должен вызвать класс приложения →
                <i>Application.INSTANCE.dataSource()</i>.
            </li>
            <li>
                Если ваша программа становится больше, и вы получаете все больше
                и больше зависимостей, у вас будет один монстр класс
                Application.java, который обрабатывает все ваши зависимости. В
                этот момент необходимо разделить зависимости на несколько
                классов / фабрик и т.д.
            </li>
        </ol>

        <h3>Инверсия управления (IoC, Inversion of Control)</h3>

        <p>
            Было бы неплохо, если бы вам в классе <i>UserDAO</i> вообще не
            приходилось беспокоиться о поиске зависимостей. Вместо того, чтобы
            активно вызывать <i>Application.INSTANCE.dataSource()</i>, ваш
            <i>UserDAO</i> мог как-то уведомлять, что он ему нужен, но больше не
            контролирует, когда / как / откуда он его получает?
        </p>

        <p>
            Это то, что называется <b>инверсией управления</b> (Inversion of
            Control).
        </p>

        <p>
            Посмотрим, как может выглядеть наш класс <i>UserDAO</i>, с новым
            конструктором.
        </p>

        <pre>
import javax.sql.DataSource;

